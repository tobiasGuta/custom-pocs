def string_payload(s):
    s_bytes = s.encode("utf-8")
    return struct.pack(">I", len(s_bytes)) + s_bytes

def build_packet(payload):
    padding_length = 4
    total_length = len(payload) + padding_length + 1
    packet = struct.pack(">I", total_length)
    packet += bytes([padding_length])
    packet += payload
    packet += b"\x00" * padding_length
    return packet

